String tr(String key, [Map<String, String>? args]) {
  var s = _loc[_lang]?[key] ?? key;
  if (args != null) {
    for (final e in args.entries) {
      s = s.replaceAll('{${e.key}}', e.value);
    }
  }
  return s;
}

void setLang(String lang) {
  if (_loc.containsKey(lang)) _lang = lang;
}

